package cn;

public class Calculator {
    /**
     * 计算两数之和
     * @param a
     * @param b
     * @return a+b
     */
    public int add(int a,int b){
        return a+b;
    }

    /**
     * 计算两数之差
     * @param a
     * @param b
     * @return a-b
     */
    public int subtract(int a,int b){
        return a-b;

    }
}
